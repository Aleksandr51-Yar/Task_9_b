# Получаем элементы первого списка от пользователя
list1 = input("Введите элементы 1-го списка (через запятую): ").split(',')

# Удаляем лишние пробелы вокруг элементов первого списка
list1 = [item.strip() for item in list1]

# Получаем элементы второго списка от пользователя
list2 = input("Введите элементы 2-го списка (через запятую): ").split(',')

# Удаляем лишние пробелы вокруг элементов второго списка
list2 = [item.strip() for item in list2]

# Удаляем элементы из первого списка, которые есть во втором списке
for item in list2:
    while item in list1:
        list1.remove(item)

# Выводим результат
print("Результат:", list1)
